/** Authority level: how citable/authoritative the document is. */
export const AUTHORITY_LEVEL_OPTIONS: { value: string; label: string }[] = [
  { value: 'contract_source_of_truth', label: 'Authoritative — contract or official source; citable as evidence' },
  { value: 'operational_suggested', label: 'Operational guidance — suggested use; internal reference only' },
  { value: 'fyi_not_citable', label: 'Informational only — e.g. from web; not citable as evidence' },
]

/** Get human-readable label for an authority level value. */
export function getAuthorityLevelLabel(value: string | null | undefined): string {
  if (!value) return ''
  const opt = AUTHORITY_LEVEL_OPTIONS.find(o => o.value === value)
  return opt ? opt.label : value
}

/** Get label for a state code. */
export function getStateLabel(code: string | null | undefined): string {
  if (!code) return ''
  const opt = STATE_OPTIONS.find(o => o.value === code)
  return opt ? `${opt.label} (${code})` : code
}
